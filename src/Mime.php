   /**
    * Get a content type from a file extension
    * 
    * @param string $ext A file extension without the dot
    * @param string $default A fallback type to return
    * @return string
    */
   public static function getTypeFromExtension(
      $ext, 
      $default = 'application/octet-stream'
   )
   {
      $ext = strtolower($ext);
      if (array_key_exists($ext, self::$extensions)) {
         $type = self::$extensions[$ext];
         return is_array($type)
            ? $type[0]
            : $type;
      }
      return $default;
   }

   /**
    * Get a file extension from a content type
    * 
    * @param string $type The mime or content type to search for
    * @param string $default A fallback to extension return
    * @return string
    */
   public static function getExtension($type, $default = 'unknown')
   {
      return array_key_exists($type, self::$types)
         ? self::$types[$type] 
         : $default;
   }

